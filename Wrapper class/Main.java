public class Main {
    public static void main(String[] args) {

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "John";

        if(a == true) {
            System.out.println("This is true");
        } 
        
        if (b == '@') {
            System.out.println("This is an '@'");
        } 
        
        if (c == 123) {
            System.out.println("This is 123");
        } 
        
        if (d == 3.14) {
            System.out.println("This is 3.14");
        } 
        
        if (e == "John") {
            System.out.println("This is 'John'");
        } 
    }
}